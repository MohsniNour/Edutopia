package Entities;

/**
 *
 * @author ADMIN
 */
public class Course {
    
    private String id;
    private String name;

    public Course() {
    }

    public Course(String name) {
        this.name = name;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    @Override
    public String toString() {
        return "Course{" + "name=" + name + '}';
    }
    
    
    
}
